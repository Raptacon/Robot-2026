def _make_mock_subsystem(name):
    """Create a mock subsystem with common lifecycle methods."""
    sub = MagicMock()
    sub.name = name
    return sub


def _failing_creator(subs):
    """Creator that simulates a hardware failure."""
    raise RuntimeError("CAN bus timeout — simulated hardware failure")


def _build_swerve_manifest(
    module_creators=None,
    drivetrain_creator=None,
    turret_creator=None,
):
    """
    Build a manifest matching the real robot topology:
    4 swerve modules (enabled) → drivetrain (enabled, depends on modules) → turret (enabled, independent).

    Any creator can be replaced to inject failures.
    """
    module_names = [
        "swerve_module_frontLeft",
        "swerve_module_frontRight",
        "swerve_module_backLeft",
        "swerve_module_backRight",
    ]

    if module_creators is None:
        module_creators = [
            lambda subs, n=n: _make_mock_subsystem(n)
            for n in module_names
        ]
    if drivetrain_creator is None:
        drivetrain_creator = lambda subs: _make_mock_subsystem("drivetrain")
    if turret_creator is None:
        turret_creator = lambda subs: _make_mock_subsystem("turret")

    entries = [
        SubsystemEntry(
            name=name,
            default_state=SubsystemState.enabled,
            creator=creator,
        )
        for name, creator in zip(module_names, module_creators)
    ]
    entries.append(SubsystemEntry(
        name="drivetrain",
        default_state=SubsystemState.enabled,
        creator=drivetrain_creator,
        dependencies=module_names,
    ))
    entries.append(SubsystemEntry(
        name="turret",
        default_state=SubsystemState.enabled,
        creator=turret_creator,
    ))

    return entries


def _make_registry(entries):
    """Create a SubsystemRegistry with NT state mocked to use default states."""
    holders = {}

    def get_holder(name):
        if name not in holders:
            h = MagicMock()
            h.state = ""  # empty → use default_state
            holders[name] = h
        return holders[name]

    with patch(
        "utils.subsystem_factory._get_state_holder",
        side_effect=get_holder,
    ):
        return SubsystemRegistry(entries)


def _simulate_robot_lifecycle(registry):
    """
    Simulate the robot cycling through all modes, calling the same
    registry lifecycle methods that RobotSwerve and robot.py invoke.

    Calls:
      disabledInit → (robotPeriodic) →
      autonomousInit → autonomousPeriodic (10 cycles with robotPeriodic) →
      teleopInit → teleopPeriodic (10 cycles with robotPeriodic) →
      disabledInit
    """
    # --- disabled ---
    registry.run_all_disabled_init()
    registry.run_all_telemetry()

    # --- autonomous ---
    # autonomousInit: RobotSwerve selects auto command (no registry call)
    for _ in range(10):
        registry.run_all_telemetry()

    # --- teleop ---
    registry.run_all_teleop_init()
    for _ in range(10):
        registry.run_all_telemetry()
        registry.run_all_teleop_periodic()

    # --- back to disabled ---
    registry.run_all_disabled_init()
    registry.run_all_telemetry()


class TestRobotDegradedSwerveModule(unittest.TestCase):
    """
    Robot lifecycle when a swerve module fails to create.
    Generated by Claude.
    """

    def setUp(self):
        _registry_state_classes.clear()

    def test_one_module_fails_robot_cycles(self):
        """
        One swerve module throws during creation. The drivetrain is
        skipped (missing dependency), but the turret and other modules
        are created. Robot cycles through all modes without crashing.
        """
        module_creators = [
            _failing_creator,                                  # frontLeft fails
            lambda subs: _make_mock_subsystem("frontRight"),
            lambda subs: _make_mock_subsystem("backLeft"),
            lambda subs: _make_mock_subsystem("backRight"),
        ]
        entries = _build_swerve_manifest(module_creators=module_creators)
        registry = _make_registry(entries)

        # Failed module → None, drivetrain skipped, turret still alive
        self.assertIsNone(registry.get("swerve_module_frontLeft"))
        self.assertIsNone(registry.get("drivetrain"))
        self.assertIsNotNone(registry.get("turret"))

        # Full lifecycle — must not raise
        _simulate_robot_lifecycle(registry)

    def test_all_modules_fail_robot_cycles(self):
        """
        All 4 swerve modules throw during creation. Drivetrain is
        skipped. Turret still works. Robot cycles without crashing.
        """
        entries = _build_swerve_manifest(
            module_creators=[_failing_creator] * 4,
        )
        registry = _make_registry(entries)

        self.assertIsNone(registry.get("drivetrain"))
        self.assertIsNotNone(registry.get("turret"))

        _simulate_robot_lifecycle(registry)


class TestRobotDegradedDrivetrain(unittest.TestCase):
    """
    Robot lifecycle when the drivetrain itself fails to create.
    Generated by Claude.
    """

    def setUp(self):
        _registry_state_classes.clear()

    def test_drivetrain_fails_robot_cycles(self):
        """
        All modules succeed but the drivetrain throws (e.g. gyro init
        failure). Modules and turret remain. Robot cycles without crashing.
        """
        entries = _build_swerve_manifest(
            drivetrain_creator=_failing_creator,
        )
        registry = _make_registry(entries)

        # All modules created, drivetrain failed, turret still alive
        self.assertIsNotNone(registry.get("swerve_module_frontLeft"))
        self.assertIsNone(registry.get("drivetrain"))
        self.assertIsNotNone(registry.get("turret"))

        _simulate_robot_lifecycle(registry)

    def test_drivetrain_fails_active_subsystems_correct(self):
        """
        active_subsystems excludes the failed drivetrain and includes
        all other successfully created subsystems.
        """
        entries = _build_swerve_manifest(
            drivetrain_creator=_failing_creator,
        )
        registry = _make_registry(entries)

        active = registry.active_subsystems
        self.assertNotIn("drivetrain", active)
        self.assertIn("swerve_module_frontLeft", active)
        self.assertIn("turret", active)


