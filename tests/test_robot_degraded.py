class _OverrunHarness:
    """
    Minimal harness that replicates MyRobot's overrun detection logic.
    Avoids instantiating the real TimedCommandRobot C++ base class.
    """

    def __init__(self):
        self.overrun_alert = wpilib.Alert(
            "Loop overrun test", wpilib.Alert.AlertType.kWarning
        )
        self.overrun_count = 0
        self.loop_timer = wpilib.Timer()
        self.loop_timer.start()
        self.container = MagicMock()

    def robotPeriodic(self):
        """Same overrun check as MyRobot.robotPeriodic."""
        # Simulate container work (no-op via mock)
        self.container.robotPeriodic()

        period = RobotConstants.kPeriodicPeriodSec
        overran = self.loop_timer.hasElapsed(period)
        if overran:
            self.overrun_count += 1
        self.overrun_alert.set(overran)
        self.loop_timer.reset()


class TestLoopOverrun(unittest.TestCase):
    """
    Test that loop overrun detection works using wpilib.Timer.
    Generated by Claude.

    Uses sim timing control to advance FPGA time past the loop period
    and verify the overrun alert and counter respond correctly.
    """

    @classmethod
    def setUpClass(cls):
        wpilib.simulation.pauseTiming()

    def setUp(self):
        wpilib.simulation.restartTiming()
        wpilib.simulation.pauseTiming()

    def test_no_overrun_within_period(self):
        """No overrun when loop completes within the period."""
        harness = _OverrunHarness()
        period = RobotConstants.kPeriodicPeriodSec

        wpilib.simulation.stepTiming(period * 0.5)
        harness.robotPeriodic()

        self.assertEqual(harness.overrun_count, 0)

    def test_overrun_detected(self):
        """Overrun detected when FPGA time exceeds the loop period."""
        harness = _OverrunHarness()
        period = RobotConstants.kPeriodicPeriodSec

        wpilib.simulation.stepTiming(period * 2)
        harness.robotPeriodic()

        self.assertEqual(harness.overrun_count, 1)

    def test_overrun_count_accumulates(self):
        """Overrun counter increments each time an overrun occurs."""
        harness = _OverrunHarness()
        period = RobotConstants.kPeriodicPeriodSec

        for _ in range(3):
            wpilib.simulation.stepTiming(period * 2)
            harness.robotPeriodic()

        self.assertEqual(harness.overrun_count, 3)

    def test_overrun_clears_after_good_loop(self):
        """Alert clears when a loop completes within the period."""
        harness = _OverrunHarness()
        period = RobotConstants.kPeriodicPeriodSec

        # Trigger an overrun
        wpilib.simulation.stepTiming(period * 2)
        harness.robotPeriodic()
        self.assertEqual(harness.overrun_count, 1)

        # Next loop is fast
        wpilib.simulation.stepTiming(period * 0.5)
        harness.robotPeriodic()

        # Count stays at 1 (no new overrun)
        self.assertEqual(harness.overrun_count, 1)


