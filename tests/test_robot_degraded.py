"""
System tests: robot survives subsystem creation failures.
Generated by Claude.

Verifies the robot can cycle through disabled → autonomous → teleop
when swerve module or drivetrain creation throws an exception.

Uses the real SubsystemRegistry with mock subsystem creators that
mirror the swerve module → drivetrain → turret topology. After the
registry handles creation failures, the test simulates the full
robot lifecycle (the same calls RobotSwerve makes in each mode).
"""

